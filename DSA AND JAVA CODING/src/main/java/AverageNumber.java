/*Write a function to print the average of 3 numbers.

Input 1: a = 1, b = 2, c = 3
Output 1: 2
Explanation 1: Average = (1 + 2 + 3) / 3 = 2

Input 2: a = 1, b = 2, c = 6
Output 2: 3
Constraints:
-216 <= a <= 216-1
-216 <= b <= 216-1
-216 <= c <= 216-1
*/

public class AverageNumber {
        static int solve(int a,int b,int c){
            return ((a+b+c)/3);

        }

}
